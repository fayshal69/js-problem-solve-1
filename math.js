// maximum value
const max = Math.max(45, 120, -90, 0, 12, 3, 100, 30, 3);
console.log(max);

// minimum value
const min = Math.min(45, 120, -90, 0, 12, 3, 100, 30, 3);
console.log(min);

// return absolute value
console.log(Math.abs(5 - 10));

// rounded a number
console.log(Math.round(2.15));
console.log(Math.round(2.75));

// ceiling a number
console.log(Math.ceil(2.15));
console.log(Math.ceil(2.75));

// flooring a number
console.log(Math.floor(2.15));
console.log(Math.floor(2.75));

// return a random number between 0 to 1
console.log(Math.random());